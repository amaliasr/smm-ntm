    function separateString(str) {
        const separatedString = str.replace(/([A-Z])/g, ' $1');
        return separatedString.trim();
    }

    function groupingListPlan(dataArray) {
        const data = dataArray

        const groupData = (arr) => {
            const result = {
                'TelahDibuat': 0,
                'Draft': 0,
                'Selesai': 0
            };

            arr.forEach(item => {
                if (item.is_created == 1 && item.is_draft == 0 && item.is_done == 0) {
                    result['TelahDibuat']++;
                } else if (item.is_created == 1 && item.is_draft == 1 && item.is_done == 0) {
                    result['Draft']++;
                } else if (item.is_created == 1 && item.is_draft == 1 && item.is_done == 1) {
                    result['Selesai']++;
                }
            });

            return result;
        };

        groupedData = groupData(data);

        var html = ''
        html += '<div class="row">'
        html += '<div class="col-auto statusLine text-small pb-2 align-self-center fw-bold filter-border" style="cursor:pointer" onclick="statusLine(' + "'all'" + ')" id="colStatusLineall">'
        html += 'Semua <span class="statusLineIcon ms-1 p-1 rounded bg-light-blue text-white" id="statusLineIconall">' + dataArray.length + '</span>'
        html += '</div>'
        for (const v in groupedData) {
            html += '<div class="col-auto statusLine text-small pb-2 align-self-center text-grey" style="cursor:pointer" onclick="statusLine(' + "'" + v + "'" + ')" id="colStatusLine' + v + '">'
            html += separateString(v)
            if (groupedData[v] != 0) {
                html += '<span class="statusLineIcon ms-1 p-1 rounded bg-light text-grey" id="statusLineIcon' + v + '">' + groupedData[v] + '</span>'
            }
            html += '</div>'
        }
        html += '</div>'
        $('#statusLine').html(html)
        formListPlan(dataArray)
    }

    function statusLine(status) {
        if (status == 'all') {
            var data = data_plan_filtered
        } else {
            var is_created = 0;
            var is_draft = 0;
            var is_done = 0;
            if (status == 'TelahDibuat') {
                is_created = 1;
                is_draft = 0;
                is_done = 0;
            } else if (status == 'Draft') {
                is_created = 1;
                is_draft = 1;
                is_done = 0;
            } else {
                is_created = 1;
                is_draft = 1;
                is_done = 1;
            }
            var data = data_plan_filtered.filter((v, k) => {
                if (v.is_created == is_created && v.is_draft == is_draft && v.is_done == is_done) return true
            })
        }
        coloringStatusLine(status)
        formListPlan(data)
    }

